package training;

public class dtypes {
	public static void main(String[] args) {
		int a=10;
		float b=10.0f;
		double c=10.0d;
		char d='A';
		String e="TW!";
		byte f=2;
		short g=1;
		long h=100;
		boolean i=true;
		
		System.out.println("This is an integer and it holds the value "+a);
		System.out.println("This is a float and it holds the value "+b);
		System.out.println("This is a double and it holds the value "+c);
		System.out.println("This is a char and it holds the value "+d);
		System.out.println("This is a String and it holds the value "+e);
		System.out.println("This is a byte and it holds the value "+f);
		System.out.println("This is a short and it holds the value "+g);
		System.out.println("This is a long and it holds the value "+h);
		System.out.println("This is a boolean and it holds the value "+i);
		

	
	}
}
